    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:

        // advanced search
        String name = tbName.getText();
        String sPrice = tbPrice.getText();
        String color = taColor.getText();
        boolean inStock = cbInStock.isSelected();
        Date date1 = dateChooser1.getDate();
        Date date2 = dateChooser2.getDate();
        String comboboxProductType = cbProductType.getSelectedItem().toString();

        Double price = null;
        if (sPrice.length() > 0) {
            try {
                price = Double.parseDouble(sPrice);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(AdministrareProdus.this, "Price should be a number");
                price = null;
            }
        }

        PriceCompare priceCompare;
        if (rbLess.isSelected()) {
            priceCompare = PriceCompare.LESS;
        } else if (rbEqual.isSelected()) {
            priceCompare = PriceCompare.EQUAL;
        } else {
            priceCompare = PriceCompare.MORE;
        }

        ProdusDAO pDao = new ProdusDAO();
        List<Produs> produse = pDao.advancedSearchProducts(name, price, priceCompare, color, inStock, date1, date2, comboboxProductType);
        refreshTable(produse, true);

    }//GEN-LAST:event_btnSearchActionPerformed

