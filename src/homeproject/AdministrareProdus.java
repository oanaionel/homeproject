    // determine spinner maximum number
    public int maxDetermination(List<Produs> produse) {
        if (produse.size() == 0) {
            return 1;
        }
        if (produse.size() % maxLimit == 0) {
            return (produse.size() / maxLimit);
        } else {
            return ((produse.size() / maxLimit) + 1);
        }
    }

    // populate table
    private void refreshTable(List<Produs> produse, boolean changed) {
        // array to limit list to 5 elements
        List<Produs> limitList = new ArrayList<>();

        // used for pagination
        if (changed) {
            generalList = produse;
            // SpinnerNumberModel(current spinner value, min, max, step)
            SpinnerModel moodelSpinner = new SpinnerNumberModel(1, 1, maxDetermination(produse), 1);
            spinnerPage.setModel(moodelSpinner);

        }
        //column names
        String[] columnNames = {"Nume Produs", "Pret", "Culoare", "Pe Stoc", "Data Expirare",
            "Nume Tip Produs", "Descriere", "Select"};
        //  column types for sorting the table
        final Class<?>[] columnClasses = new Class<?>[]{String.class, Double.class, String.class, String.class,
            Date.class, String.class, String.class, Boolean.class};
        // set column names
        DefaultTableModel model = new DefaultTableModel() {

            @Override
            public int getColumnCount() {
                return columnNames.length;
            }

            @Override
            public String getColumnName(int index) {
                return columnNames[index];
            }

            @Override
            public Class<?> getColumnClass(int column) {
                return columnClasses[column];
            }
        };
        // get current spinner value
        Integer currentValue = (Integer) spinnerPage.getValue();
        // populate limitList with values to be displayed on a single page
        int realMaxLimit = Math.min(produse.size(), currentValue * maxLimit);
        limitList = produse.subList((currentValue - 1) * maxLimit, realMaxLimit);
        Iterator<Produs> it = limitList.iterator();
        while (it.hasNext()) {
            Produs p = it.next();
            Object[] rowData = {p.getNumeProdus(), p.getPretProdus(),
                p.getCuloare(), Boolean.toString(p.isPeStoc()), p.getDataExpirare(),
                p.getTp().getNumeTipProdus(), p.getTp().getDescriere(), Boolean.FALSE};
            model.addRow(rowData);
        }

        tabelProduse.setModel(model);
        // sortable table
        tabelProduse.setAutoCreateRowSorter(true);
        RowSorter<DefaultTableModel> sorter = new TableRowSorter<DefaultTableModel>(model);
        tabelProduse.setRowSorter(sorter);
    }


